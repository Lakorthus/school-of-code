//create a new Set object
const set = new Set([1, 2, 3]);
//log the set object
console.log(set); // Set {1, 2, 3}

//create a new Map object
const map = new Map([
    ['Alice', 20],
    ['Bob', 21],
    ['Charlie', 22]
  ]);
console.log(map); // Map { 'Alice' => 20, 'Bob' => 21, 'Charlie' => 22 }

//This means that a set can only contain unique values, while a map can contain multiple values for the same key.